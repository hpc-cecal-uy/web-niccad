		<div style="background-color: #EEEEEE; padding-left: 5px; padding-right: 5px; padding-top: 2px; padding-bottom:2px; font-size: 16px;">
			Aplicación del modelo WRFARW a la predicción de la generación de energía eléctrica en parques eólicos</div>
		<div style="padding-left: 5px; padding-right: 5px; padding-top: 2px; padding-bottom:2px; font-size: 12px;">
			<div style="padding-top: 1px; padding-bottom:1px;">
				<span style="font-weight: bold;">Grupo:</span> 
				Ingeniería del Viento.
			</div>
			<div style="padding-top: 1px; padding-bottom:1px;">
				<span style="font-weight: bold;">Investigadores:</span> 
				Alejandro Gutiérrez IMFIA-FING, Gabriel Cazes IMFIA-FING, José Cataldo IMFIA-FING.
			</div>
			<div style="padding-top: 1px; padding-bottom:1px;">
				<span style="font-weight: bold;">Resumen</span>: 
				<span  style="font-style: italic;">En la medida que la potencia eólica instalada en el
				sistema eléctrico nacional creciera, resulta necesario desarrollar
				herramientas de predicción del recurso eólico. La predicción
				de la generación de los parques eólicos instalados y que se
				instalen en el futuro permitirá conocer con menor incertidumbre
				la cantidad de energía eléctrica que se suministrara a la red
				partir de la energía eólica. Los aerogeneradores de mayor
				porte y potencia construidos en la actualidad para
				aprovechamientos conectados a la red, que se encuentren en una
				faz de desarrollo comercial disponen de torres de entre 30 y 160
				metros de altura. En el este trabajo se presenta resultados de
				modelación numérica a partir de la aplicación WRF-ARW-
				versión 3.2.1, en dos sitios, uno de ellos con una topografía
				simple y otro con topografía compleja en el cual se tiene instalado
				un parque eólico de 20 MW. Los resultados de la modelación
				numérica fueron contrastados con mediciones en campo de
				velocidad y dirección de viento.</span>
