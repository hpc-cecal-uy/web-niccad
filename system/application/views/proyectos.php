			<div style="padding-top: 1px; padding-bottom:1px;">
				<span style="font-weight: bold;">Resumen</span>: 
				<span  style="font-style: italic;">Las redes overlay son una realidad instaurada
				desde hace un buen tiempo en las organizaciones y la vida diaria.
				Este tipo de redes poseen la particularidad de estar formadas por
				dos (o más) capas. En este proyecto se pretende atacar el
				problema del diseño óptimo en costos de una red multioverlay
				tolerante a fallas. En particular interesa resolver el problema
				para un escenario real planteado por la empresa Antel, la
				compañía nacional de telecomunicaciones. En este escenario se
				plantea el diseño de una red de datos MPLS construida sobre una
				infraestructura de transporte de múltiples tecnologías (SDH y DWDM).<br/>
				Puede demostrarse que el problema a tratar es NP-difícil, por
				lo que se utiliza un enfoque basado plenamente en la aplicación
				de distintas metaheurísticas para resolverlo. Concretamente, se
				exploran las técnicas Tabu Search y Algoritmos Genéticos, así
				como un algoritmo híbrido de ambas. Con el objetivo de mejorar
				los tiempos de ejecución se experimentó con técnicas de
				paralelismo sobre el Algoritmo Genético y el híbrido
				desarrollados. La evaluación de las técnicas exploradas marca
				una clara ventaja del Tabu Search sobre el Algoritmo Genético y
				el híbrido implementados.</span>
