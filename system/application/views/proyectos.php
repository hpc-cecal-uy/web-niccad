			<div style="padding-top: 1px; padding-bottom:1px;">
				<span style="font-weight: bold;">Resumen</span>: 
				<span  style="font-style: italic;">El continuo crecimiento de la demanda eléctrica
				trae aparejado la necesidad de acompañar dicho crecimiento con
				la instalación de nuevas centrales de generación. El diseño de un
				plan de expansión óptimo es un problema complejo por los montos 
				de inversiones involucrados y porque los plazos de construcción 
				de nuevas centrales van de 2 a 8 años dependiendo de la tecnología. 
				Esto hace que la decisión del plan involucra un riesgo alto 
				asociado a las hipótesis que se asuma sobre el futuro. En este
				trabajo se muestra un algoritmo genético aplicado a la optimización 
				del plan de inversiones. El objetivo es mostrar un método y
				explorar su potencial con miras a la investigación de mejores métodos 
				a sabiendas que el algoritmo implementado es seguramente mejorable.</span>
