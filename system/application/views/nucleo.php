	<h3><a name="integracion">Integración</a></h3>
	<div class="contenido">
		El NICCAD pretende ser una estructura transversal que promueva la integración de investigadores con formación variada, tratando de aportar sus propios puntos de vista sobre la utilización de las técnicas matemáticas y de computación para la resolución de diversos problemas científicos.
	</div>
	<h3><a name="cometidos">Cometidos</a></h3>
	<div class="contenido">
		Los cometidos del NICCAD involucran:
		<ul>
			<li>Promover la investigación interdisciplinaria que permita la resolución eficiente de	problemas científicos complejos.</li>
			<li>Fomentar la integración entre investigadores de diversas disciplinas para crear una	visión integrada del uso de las técnicas de computación científica y de alto desempeño.</li>
			<li>Impulsar el desarrollo de actividades de formación en las áreas de matemática aplicada, modelación numérica, simulación, y computación de alto desempeño.</li>
			<li>Difundir la utilización de las técnicas de computación de alto desempeño y la utilización de las infraestructuras computacionales de alto desempeño (clusters de computadores, plataformas distribuidas de computación grid y cloud).</li>
			<li>Formar y capacitar recursos humanos para colaborar con las actividades de orientación de los investigadores que utilizan las técnicas de modelado matemático, simulación y computación de alto desempeño.</li>
		</ul>
	</div>
	<h3><a name="estructura">Estructura</a></h3>
	<div class="contenido">
		El NICCAD estará compuesto por grupos de investigación formados por investigadores de los
		institutos de Facultad de Ingeniería y de otros servicios de la Universidad de la República.
		Asimismo, podrán integrarse investigadores asociados de otras Universidades (locales y
		extranjeras). También podrán integrar el núcleo estudiantes de posgrado de las diversas áreas
		de investigación relacionadas con la temática de interés.<br/>
		<br />
		El NICCAD tendrá un responsable científico, que representará al núcleo en instancias formales
		y ante las autoridades de la Universidad de la República, y un responsable académico que
		tendrá a su cargo las principales actividades relacionadas con la redacción y ejecución de los
		proyectos interdisciplinarios, la formación de investigadores y la difusión de las estrategias de
		trabajo en el área.<br />
